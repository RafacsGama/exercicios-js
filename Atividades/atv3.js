function potencia(base, expoente){
    let resultado = Math.pow(base, expoente)
    return resultado
}
console.log(potencia(2,2))

function potencia2(base, expoente){
    resultado = base ** expoente
    return resultado
}
console.log(potencia2(2,3))

function expon(a, b){
    return Math.pow(a, b)
}
console.log(expon(2,4))